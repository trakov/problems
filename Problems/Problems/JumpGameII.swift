        let n = nums.count
        guard n > 1 else { return 0 }
        var result = 1
        var reach = nums[0]
        var cur = 1
        while reach < n - 1 {
            var newReach = 0
            while cur <= reach {
                newReach = max(newReach, cur + nums[cur])
                cur += 1
            }
            result += 1
            reach = newReach
        }
        return result
    }

