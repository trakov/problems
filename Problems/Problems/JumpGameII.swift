    func jump2(_ nums: [Int]) -> Int {
        let n = nums.count
        guard n > 1 else { return 0 }
        var nums = nums
        for i in 1..<n {
            nums[i] = max(nums[i] + i, nums[i-1])
        }
        var i = 0
        var result = 0
        while i < n - 1 {
            result += 1
            i = nums[i]
        }
        return result
    }
    
